# Convertidor de millas a kilómetros y viceversa.

# Asignando el valor de millas.
miles = 7.38
# Convertir millas a kilómetros.
kilometers = miles * 1.61

### Tu código debe ir aquí.
print(miles, "millas son", round(kilometers, 2), "kilómetros")

# Asignando el valor de kilómetros.
kilometers = 12.25
# Convertir kilómetros a millas.
miles = kilometers / 1.61

### Tu código debe ir aquí.
print(kilometers, "kilómetros son", round(miles, 2), "millas")
